 Servicios ofrecidos por la Psicóloga Angie Rojas

Como psicóloga clínica y organizacional, Angie Rojas ofrece un acompañamiento integral, humano y ético, enfocado en el bienestar emocional, personal y familiar. Sus servicios están dirigidos a adolescentes, adultos, parejas y grupos, y se desarrollan desde un enfoque cognitivo-conductual y sistémico, adaptándose a las necesidades particulares de cada persona. Entre sus principales servicios se destacan:
🔹 Terapia individual

Atención personalizada para el tratamiento de trastornos mentales como:

    Ansiedad y ataques de pánico

    Depresión y trastornos del ánimo

    Trastorno obsesivo-compulsivo (TOC)

    Trastorno límite de la personalidad (TLP)

    Trastorno bipolar

    Adicciones y conductas compulsivas

Estas sesiones permiten al paciente identificar y transformar patrones de pensamiento, emociones y comportamientos que afectan su vida diaria.
🔹 Terapia familiar y de pareja

Espacios de escucha y acompañamiento donde se trabajan:

    Conflictos de pareja

    Falta de comunicación

    Dificultades con hijos o entre generaciones

    Procesos de separación o duelo familiar

Se busca fortalecer los vínculos, mejorar el diálogo y construir relaciones más saludables.
🔹 Atención en crisis emocionales

Intervención breve y eficaz para momentos de desbordamiento emocional, pérdida, eventos traumáticos o situaciones de alto impacto psicológico.
🔹 Psicología organizacional

Orientación y apoyo a empresas y organizaciones en procesos como:

    Evaluación y selección de personal

    Bienestar laboral y clima organizacional

    Intervención psicosocial individual o grupal

    Talleres de salud mental, liderazgo y manejo del estrés

🔹 Modalidad de atención

    Atención por consulta externa presencial

    También disponible en modalidad virtual (videollamadas) para mayor comodidad y accesibilidad